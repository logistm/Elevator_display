void LCDClear()
{
	uint16_t zero = 0;
	uint8_t n=0;

#ifdef MATRIX_SMALL
	n=2;
#endif
#ifdef MATRIX_BIG
	n=3;
#endif

	for(uint8_t i=0;i<n;i++)
	{
		SPI3SendData(&zero);
	}
	HAL_GPIO_WritePin(GPIOB, LATCH_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOB, LATCH_Pin, GPIO_PIN_RESET);
}

void LCDFirstZero()
{
	uint16_t zero = 0;
	SPI3SendData(&zero);
	HAL_GPIO_WritePin(GPIOB, LATCH_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOB, LATCH_Pin, GPIO_PIN_RESET);
}

void LCDLatch(uint8_t delay)
{
	HAL_GPIO_WritePin(GPIOB, LATCH_Pin, GPIO_PIN_SET);
	HAL_Delay(delay);
	HAL_GPIO_WritePin(GPIOB, LATCH_Pin, GPIO_PIN_RESET);
}
