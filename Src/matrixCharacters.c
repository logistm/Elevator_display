void LCDWriteCharacter_small(uint8_t data1[16][8], uint8_t data2[16][8])
{
	uint16_t columnData1 = 0, columnData2 = 0;
	uint8_t i = 0, j = 0, k = 0, h = 0;

	h = 7;

	for(i=0; i<8; i++)
	{
		columnData1 = 0;
		columnData2 = 0;
		k = 15;

		for(j=0; j<16; j++)
		{
			columnData1 |= data1[k][h] << j;
			columnData2 |= data2[k][h] << j;
			k--;
		}
		h--;

		LCDColumnON(i);
		SPI3SendData(&columnData1);
		SPI3SendData(&columnData2);
		LCDLatch(1);
		LCDColumnOFF(i);
		LCDClear();
	}
}

void LCDWriteCharacter(uint8_t data1[16][8], uint8_t data2[16][8], uint8_t data3[16][8])
{
	uint16_t columnData1 = 0, columnData2 = 0, columnData3 = 0;
	uint8_t i = 0, j = 0;

	for(i=0; i<8; i++)
	{
		columnData1 = 0;
		columnData2 = 0;
		columnData3 = 0;

		for(j=0; j<16; j++)
		{
			columnData1 |= data1[j][i] << j;
			columnData2 |= data2[j][i] << j;
			columnData3 |= data3[j][i] << j;
		}

		LCDColumnON(i);
		SPI3SendData(&columnData3);
		SPI3SendData(&columnData2);
		SPI3SendData(&columnData1);
		LCDLatch(1);
		LCDColumnOFF(i);
		LCDClear();
	}
}

